#include"3-calc.h"
/**
 * op_add- adds two intgers
 * @a:first intger
 * @b:second intger
 * Return:sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub-subtracts two intgers
 * @a:first intger
 * @b:second intger
 * Return:sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul- multiply two intgers
 * @a:first intger
 * @b:second intger
 * Return:the product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div- divides two intgers
 * @a:first intger
 * @b:second intger
 * Return:division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod- modulo two intgers
 * @a:first intger
 * @b:second intger
 * Return:remainder
 *
 */

int op_mod(int a, int b)
{
	return (a % b);
}
